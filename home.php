			Imagine you are an alien living on a planet 50 light from Earth. You use a very, very powerful telescope to look at Earth. If you look at Earth today, you would see Earth the way it did, does, or will look in the year:
			<ul>
				<li>1913</li>
				<li>1963</li>
				<li>1988</li>
				<li>2013</li>
				<li>2063</li>
			</ul>
